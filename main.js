// ===================== CHAT =====================
function startChat(topic) {
  currentTopic = topic;
  turnCount = 0;
  chatHistory = []; // Reset history for new session
  document.getElementById('chatAvatar').textContent = currentPhil.emoji;
  document.getElementById('chatName').textContent = currentPhil.name[currentLang];
  document.getElementById('chatEra').textContent = `${currentPhil.era[currentLang]} · ${currentPhil.region[currentLang]}`;
  document.getElementById('chatTopicPill').textContent = topic ? `${t('exportTopic')}: ${topic}` : t('freeChat');
  document.getElementById('chatMessages').innerHTML = '';
  
  const launch = () => {
    showScreen('screen-chat');
    pushWelcome();
    setTimeout(() => document.getElementById('chatInput').focus(), 300);
  };
  if(topic) { showAd(launch); } else { launch(); }
}

function pushWelcome() {
  appendMsg('phil', t('welcome'));
}

function appendMsg(role, text) {
  const msgs = document.getElementById('chatMessages');
  if (!msgs) return;
  const div = document.createElement('div');
  div.className = `msg ${role}`;
  const name = role === 'phil' ? currentPhil.name[currentLang] : t('exportMe');
  div.innerHTML = `
    <div class="msg-bubble-wrap">
      <div class="msg-name" style="font-size: 0.75rem; color: var(--cream3); margin-bottom: 0.3rem;">${name}</div>
      <div class="msg-bubble">${text.replace(/\n/g,'<br>')}</div>
    </div>
  `;
  msgs.appendChild(div);
  msgs.scrollTop = msgs.scrollHeight;
}

